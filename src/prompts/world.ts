export const makeCountryStr = (name: string, context: string): string => `
Ты создаёшь государство, в котором живёт подросток по имени ${name}.  
Это может быть просто контекст, который слегка влияет на его восприятие мира.  
А может — активно формировать его убеждения, страхи, желания, стиль общения и мышления.  

Важно: результат не должен быть художественным. Пиши **простым, реалистичным языком**, как будто ты описываешь обычную страну глазами жителя или социолога.  
Избегай метафор, поэтики и стилистических украшений. Только факты, убеждения и бытовая логика.

Вот что мы знаем о подростке:
${context}

Сгенерируй описание страны строго в формате **валидного JSON** (⚠️ без Markdown, без обёрток), со следующими полями:
{
  "countryName": string,  
  "government": "демократия" | "авторитаризм" | "гибрид" | "переходный режим",
  "dominantIdeology": string,
  "culturalNorms": string[],
  "stateControlLevel": number,           // от 0 до 100
  "economicStability": number,           // от 0 до 100
  "socialMobility": number,              // от 0 до 100
  "tabooTopics": string[],
  "societalPressure": string,
  "emotionalClimate": string,
  "historicalContext": string,
  "description": string
}

⚠️ Ответ — только JSON. Без пояснений, Markdown или дополнительных символов.
`.trim();


