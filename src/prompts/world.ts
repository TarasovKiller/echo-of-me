  export const makeEducationTemplate = (
    name: string,
    age: number,
    lifeContext: string,
    countryContext: string,
    settlementContext: string,
    familyContext: string,
    socialContext: string
  ): string => `
  Ты создаёшь учебное заведение, в котором сейчас учится подросток по имени ${name}.  
  Ей ${age} лет. Это может быть школа, колледж, интернат или специализированное учреждение — в зависимости от возраста и социальной среды.
  
  Это заведение **не обязано соответствовать духу страны, города или семьи**.  
  Оно может противоречить им — по ценностям, дисциплине, атмосфере или неформальным правилам.
  
  Твоя задача — описать **реалистичную институцию**, которая:
  - встроена в местную жизнь,
  - влияет на поведение, убеждения и эмоциональные стратегии подростка,
  - может как поддерживать, так и фрустрировать её.
  
  Избегай поэтики и преувеличений. Пиши так, как если бы ты делал **социально-психологическое описание школьной среды**, понятное наблюдателю, но чувствуемое изнутри.
  
  Контекст подростка:
  ${lifeContext}
  
  Контекст страны:
  ${countryContext}
  
  Контекст города/деревни:
  ${settlementContext}
  
  Контекст семьи:
  ${familyContext}
  
  Контекст социальное окружение:
  ${socialContext}
  
  Обрати внимание на возможные противоречия:
  
  - Учебное заведение может **декларировать одно**, но быть устроено иначе (например: говорит о свободе, но на деле — контроль и страх).
  - Учебное заведение может **идти вразрез с культурой страны**: например, в авторитарной стране школа поддерживает самовыражение.
  - Учебное заведение может **контрастировать с семейными установками**: например, семья требует послушания, а школа даёт возможность быть лидером.
  - В учебной среде могут быть **допустимы темы, которые в семье или обществе табуированы** (например: психология, политика, гендер, эмоции).
  - Атмосфера в коллективе может **сильно отличаться от тона города**: в закрытом посёлке — открытая, мультикультурная школа.
  
  Если такие противоречия есть — **не избегай их**, а отрази как часть структуры заведения.
  
  Ответ строго в формате валидного JSON по следующей структуре:
  {
    "institutionName": string,
    "type": "school" | "boarding" | "college" | "religious" | "special",
    "knownFor": string,
    "hiddenRules": string[],
    "publicValues": string[],
    "attitudeToLife": string,
    "impactOnLife": string,
    "triggersLifeFear": boolean,
    "feedsHiddenDesire": boolean,
    "description": string
  }
  
  ⚠️ Только JSON. Без Markdown, заголовков или пояснений.
  `.trim();
  